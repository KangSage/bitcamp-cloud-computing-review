// 019. JSON이란?
// => JavaScript Object Notation 자바스크립트의 객체 표기법. 제이썬이라고 발음함.
"use strict";

const user = {}; // 객체를 만든다.
user.name = 'sage'; // user객체에 name이라는 프로퍼티를 만들고 값을 넣는다..
user.age = 31; // user객체에 age라는 프로퍼티를 만들고 값을 넣는다.

console.log(user);

const user2 = { name: 'sage', age: 31}; // user02를 만들면서 프로퍼티와 값을 넣는다.
console.log(user2);